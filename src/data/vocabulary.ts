// Create units of 50 words each
export const units: Unit[] = [];
const wordsPerUnit = 50;
const totalUnits = Math.ceil(allWords.length / wordsPerUnit);

for (let i = 0; i < totalUnits; i++) {
  const start = i * wordsPerUnit;
  const end = Math.min(start + wordsPerUnit, allWords.length);
  const unitWords = allWords.slice(start, end);
  
  units.push({
    id: i + 1,
    title: `Unit ${i + 1}`,
    words: unitWords,
  });
}

export const getUnitById = (id: number): Unit | undefined => {
  return units.find(unit => unit.id === id);
};

export const shuffleArray = <T>(array: T[]): T[] => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};
